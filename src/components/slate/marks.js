import React from "react";

export const CodeMark = ({ children }) => <code>{children}</code>;
export const BoldMark = ({ children }) => <strong>{children}</strong>;
export const ItalicMark = ({ children }) => <em>{children}</em>;
export const UnderlineMark = ({ children }) => <u>{children}</u>;
export const StrikethroughMark = ({ children }) => <del>{children}</del>;
